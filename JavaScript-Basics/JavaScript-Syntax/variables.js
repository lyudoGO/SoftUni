
function variablesTypes (value) {
    console.log("My name: %s //type is %s", value[0], typeof(value[0]));
    console.log("My age: %s //type is %s", value[1], typeof(value[1]));
    console.log("I am male: %s //type is %s", value[2], typeof(value[2]));
    console.log("My favorite foods are: %s //type is %s", value[3], typeof(value[3]));
}

variablesTypes(['Pesho', 22, true, ['fries', 'banana', 'cake']]);


